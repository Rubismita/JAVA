package Demo;

public class AlphabetReverseFromZtoA {

	public static void main(String[] args) {
   
        for(char i = 'z'; i >='a'; i--){
            System.out.print(i);
        }
       
        System.out.println();
        System.out.println();
        
       
        for(char i = 'Z'; i >='A'; i--){
            System.out.print(i);
        }

	}

}
